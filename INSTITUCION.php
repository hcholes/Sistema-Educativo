      <p>Nombre del plantel: <font size="3"><strong>Institución 
        Educativa Rural Porfirio Barba Jacob</strong></font></p>
      <p> Niveles: <font size="2"><strong>Preescolar, Básica: Primaria Y Secundaria 
        Y Media Académica.</strong></font></p>
      <p> Carácter: <font size="3"><strong>Oficial</strong></font></p>
      <p> Jornada: <font size="3"><strong>Completa u ordinaria – diurna</strong></font></p>
      <p>&nbsp;</p>
      <p><font size="3"><strong>RESEÑA HISTORICA</strong></font></p>
      <p align="justify">La creación del Liceo surgió de la idea del Presbítero 
        Alfredo Zea, quien en 1968 inscribió un grupo de 41 jóvenes, quienes habían 
        terminado el 5º de primaria anteriormente; esta idea fue secundada por 
        varios líderes de la comunidad.</p>
      <p align="justify"><br />
        Con esta idea se dirigieron a la Asamblea Departamental y a la Secretaría 
        de Educación, lo que posteriormente dio lugar a la creación del Liceo 
        mediante Ordenanza 33 del 30 de diciembre de 1968.</p>
      <p align="justify"><br />
        El 4 de marzo de 1969 comenzó labores el primero de bachillerato con un 
        total de 41 alumnos, anexo al Liceo Luis López de Mesa del municipio de 
        Guadalupe, por Resolución Nº 105; posteriormente, por Decreto 024 de Enero 
        17 de 1970, se anexó al Liceo Manuel José Caicedo del municipio de Barbosa 
        y ese mismo Decreto autorizó al Rector de Barbosa para abrir el segundo 
        de bachillerato. En 1971 se creó el tercero y en 1972 el cuarto de bachillerato. 
        Por falta de alumnos, en 1973, 1974 y 1975 sólo funcionó con el grupo 
        primero de bachillerato.<br />
        En Abril de 1976 se creó nuevamente el segundo de bachillerato y continuó 
        así hasta completar en el año de 1978 los cuatro grados de bachillerato 
        (Básica Secundaria).</p>
      <p align="justify"><br />
        En 1978, por Decreto Departamental 00376 de marzo 10, se crea una plaza 
        para rectoría, otra para secretaría académica y dos para profesores de 
        enseñanza media, pasando a ser una institución independiente y llevando 
        el nombre de I.D.E.M. San Pablo.<br />
        Por Decreto 01419 del 28 de septiembre de 1979 se cambia nuevamente el 
        nombre por el IDEM. PORFIRIO BARBA JACOB.<br />
        En 1989 se creó el quinto grado de enseñanza media y en 1990 se creó el 
        grado sexto, realizándose así la primera promoción de bachilleres académicos.<br />
        Con la resolución 1002 de 11 de agosto de 1997 se autoriza por primera 
        vez el convenio con el Sena para la MEDIA TÉCNICA EN SALUD, la cual funcionó 
        hasta el año 2009 con los alumnos de grado 11º y a partir del grado 10º 
        en 2009 se inició con la Media Académica.</p>
      <p align="justify"><br />
        En la resolución 0385 del 99 se autoriza certificación como agente de 
        salud y secretariado médico y clínico convenio con el SENA.<br />
        El 20 de febrero de 2003, por Resolución 1495 se fusionan la Escuela Urbana 
        San Pablo y el Liceo Porfirio Barba Jacob, bajo una sola administración, 
        con el nombre de Institución Educativa Rural &quot;Porfirio Barba Jacob&quot;, 
        conformada por 4 educadoras de la básica primaria y 8 educadores de la 
        Básica Secundaria y la Media Técnica, reestructuración que hizo el departamento 
        de Antioquia, bajo el decreto 3020 que racionalizó el servicio educativo 
        y se cancela una plaza docente para prestar dicho faltante mediante el 
        uso de hora extra para la media técnica.<br />
        Mediante resolución 811 de mayo de 2004 se entrega la guía metodológica 
        para la educación media técnica.</p>
      <p align="justify"><br />
        Para el año 2007 dado los buenos resultados académicos en las pruebas 
        ICFES el gobierno mediante decreto 2832 de agosto 18 de 2005 faculta a 
        éstos establecimientos para hacer las validaciones para estudiantes de 
        los grados 1º a 10º previa reglamentación aprobada por el consejo directivo 
        e inscrita en el Proyecto Educativo Institucional.</p>
      <p align="justify"><br />
        La participación de la comunidad con respecto a la fusión de las instituciones 
        fue muy significativa ya que los educadores y directivos del Liceo y de 
        la Escuela Urbana nos dimos a la tarea de promocionar el servicio de la 
        educación en la región, motivando a los padres de familia que tuvieran 
        hijos en la edad escolar para que los vincularan a la educación, siendo 
        para el municipio muy importante el número de estudiantes matriculados 
        para acceder a la certificación, a la racionalización del Recurso Humano 
        Docente y a la fusión de escuelas e instituciones secundarias.</p>
      <p align="justify"><br />
        El impacto de la fusión en la comunidad ha sido muy positivo, pues según 
        aportes verbales de padres de familia, han optimizado el tiempo de las 
        reuniones aprovechándolo al máximo; se ha evidenciado ahorro en los costos 
        educativos y derechos de Asociación de Padres de Familia, también se han 
        visto beneficiados en el aspecto físico puesto que se pueden ocupar y 
        compartir más espacios de la planta física para actividades culturales, 
        deportivas y recreativas, apoyados en un equipo más numeroso de docentes.</p>
      <p align="justify"><br />
        Desde el municipio, a través de la alcaldía, la secretaría de educación 
        y la unidad de asistencia técnica han brindado apoyo incondicional para 
        que la labor realizada por la institución sea meritoria e impactante con 
        la comunidad y lograr buenos resultados.</p>
      <p align="justify"><br />
        La acción Comunal, se ha vinculado muy estrechamente con la institución 
        y uno de sus locales llamado Casa de la Cultura se utiliza constantemente 
        para ensayos, reuniones y actos cívicos de la comunidad. </p>
      <p align="justify"><br />
        El 25 de Junio de 2010, mediante la Resolución 0098235 emanada de la Secretaría 
        de Educación para la Cultura de Antioquia se autoriza la Educación Preescolar 
        – grado Transición, Básica ciclos Primaria y Secundaria (grado 1º a 9º) 
        y Media académica grados 10º y 11º y se legalizan los estudios cursados 
        en el grado 10º en 2009.<br />
      </p>
